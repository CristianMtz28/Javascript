'use strict'

//Do-While

/*
En el bucle Do while primero ejecutamos el bloque de código y después comprobamos la condición para
saber si vuelve a ejecutarse el código o no.

Por ejemplo; podemos hacer el ejercicio de los años con el DoWhile
*/

var year = 28;

do {
    alert('Sólo cuando sea diferente a 20');
    year = 20;
} while (year != 20);